def generate_statistics_event_method(
    event_name: str, event_def: Dict
) -> tuple[str, str]:
    """Generate a factory method and data class for a statistics event."""
    method_name = snake_to_pascal(event_name)
    data_class_name = f"{method_name}Data"

    properties = event_def.get("properties", {})

    # Map JSON types to Python types
    type_mapping = {
        "integer": "int",
        "float": "float",
        "string": "str",
        "boolean": "bool",
    }

    # Generate data class fields
    data_class_fields = []
    for prop_name, prop_type in sorted(properties.items()):
        python_type = type_mapping.get(prop_type, "str")
        default_value = (
            "0"
            if python_type == "int"
            else (
                "0.0"
                if python_type == "float"
                else '""' if python_type == "str" else "False"
            )
        )
        data_class_fields.append(
            f"    {prop_name}: {python_type} = Field(\n"
            f'        description="{prop_name.replace("_", " ").title()}", default={default_value}\n'
            f"    )"
        )

    data_class = f"""class {data_class_name}(BaseModel):
{chr(10).join(data_class_fields) if data_class_fields else "    pass"}


"""

    # Generate parameter list for method
    param_list = ", ".join(
        f"{prop}: {type_mapping.get(prop_type, 'str')}"
        for prop, prop_type in sorted(properties.items())
    )

    # Generate data instantiation
    data_params = ", ".join(f"{prop}={prop}" for prop in sorted(properties.keys()))

    method_code = f'''    @staticmethod
    def {method_name}({param_list}) -> StatisticsEvent:
        """
        Create {event_name} statistics event.
        """
        import base64
        import json

        data = {data_class_name}({data_params}).dict()
        data_json = json.dumps(data)
        data_base64 = base64.b64encode(data_json.encode("utf-8")).decode("utf-8")

        return StatisticsEvent(
            type=StatisticsEventType.{event_name}.value,
            data_base64=data_base64,
        )

'''
    return data_class, method_code


def generate_statistics_events(events_path: Path, output_path: Path):
    """Generate statistics events file."""
    events = load_json(events_path)

    header = '''"""
Auto-generated statistics events file.
DO NOT EDIT THIS FILE MANUALLY - it is generated by generator.py
"""

import enum
from pydantic import BaseModel, Field
from .statistics_event import StatisticsEvent


'''

    # Generate enum
    enum_members = []
    for event_name in sorted(events.keys()):
        enum_members.append(f'    {event_name} = "{event_name}"')

    enum_code = f'''class StatisticsEventType(enum.Enum):
    """Enum for statistics event types."""
{chr(10).join(enum_members)}


'''

    data_classes = []
    methods = []
    for event_name, event_def in sorted(events.items()):
        data_class, method = generate_statistics_event_method(event_name, event_def)
        data_classes.append(data_class)
        methods.append(method)

    class_code = f'''class StatisticsEventFactory:
    """Factory class for StatisticsEvent instances."""

{"".join(methods)}
'''

    full_code = header + enum_code + "".join(data_classes) + class_code

    with open(output_path, "w") as f:
        f.write(full_code)

    print(f"Generated {output_path} with {len(events)} statistics events")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="Generate event factory from JSON definitions"
    )
    parser.add_argument(
        "--type",
        choices=["operational_events", "audit_records", "statistics_events"],
        required=True,
        help="Type of events to generate (operational_events, audit_records, or statistics_events)",
    )
    parser.add_argument(
        "--input",
        type=Path,
        required=True,
        help="Path to input JSON file with event definitions",
    )
    parser.add_argument(
        "--output",
        type=Path,
        required=True,
        help="Output path for generated code file",
    )

    args = parser.parse_args()

    if args.type == "operational_events":
        generate_operational_events(args.input, args.output)
    elif args.type == "audit_records":
        generate_audit_records(args.input, args.output)
    elif args.type == "statistics_events":
        generate_statistics_events(args.input, args.output)
